package ex1;

/**
 * Classe qui fournit une implementation d operations de base
 *
 */
public class Calcul {

	/** Implemente une addition
	 * @param a
	 * @param b
	 * @return a + b
	 */
	public int addition(int a, int b) {
		return a + b;
	}

	/** Implemente une soustraction
	 * @param a
	 * @param b
	 * @return a - b
	 */
	public int soustraction(int a, int b) {
		return a - b;
	}
}
